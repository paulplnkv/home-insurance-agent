export function getDocumentById(id: string): ScenarioDocument | undefined {
  return SCENARIO_DOCUMENTS.find((d) => d.id === id);
}

// What the carrier expects to see in a complete homeowners hailstorm
// claim file. Anything in this list that isn't in SCENARIO_DOCUMENTS is
// "missing" — used for ground-truth verification of the agent's missing-
// document finding.
export const REQUIRED_DOCUMENT_KINDS: readonly DocumentKind[] = [
  'fnol_transcript',
  'contractor_estimate',
  'field_inspection_report',
  'weather_verification',
  'recorded_statement',
  'mortgage_statement',
  'emergency_mitigation_receipt',
];

export function getMissingRequiredKinds(): DocumentKind[] {
  const present = new Set(SCENARIO_DOCUMENTS.map((d) => d.kind));
  return REQUIRED_DOCUMENT_KINDS.filter((k) => !present.has(k));
}
