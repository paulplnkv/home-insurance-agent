// Scenario document set — single source of truth for the six pseudo-
// documents the Cross-Document Consistency agent reads. Per PRD § Modules
// (scenario loader) and Task 7.
//
// IMPORTANT: the demo's expected-document set is SEVEN items (six present
// JSON files + one deliberately missing emergency mitigation receipt).
// The agent must classify the inventory and surface the missing receipt
// as one of its findings.
import fnolTranscript from './fnol-transcript.json';
import contractorEstimate from './contractor-estimate.json';
import fieldInspection from './field-inspection.json';
import noaaWeather from './noaa-weather.json';
import recordedStatement from './recorded-statement.json';
import mortgageStatement from './mortgage-statement.json';

export const DOCUMENT_KINDS = [
  'fnol_transcript',
  'contractor_estimate',
  'field_inspection_report',
  'weather_verification',
  'recorded_statement',
  'mortgage_statement',
  'emergency_mitigation_receipt',
] as const;

export type DocumentKind = (typeof DOCUMENT_KINDS)[number];

export const DOCUMENT_KIND_LABELS: Record<DocumentKind, string> = {
  fnol_transcript: 'FNOL transcript',
  contractor_estimate: 'Contractor estimate',
  field_inspection_report: 'Field inspection report',
  weather_verification: 'Weather verification (NOAA)',
  recorded_statement: 'Recorded statement',
  mortgage_statement: 'Mortgage statement',
  emergency_mitigation_receipt: 'Emergency mitigation receipt',
};

export interface ScenarioDocument {
  id: string;
  kind: DocumentKind;
  title: string;
  filename: string;
  // Public URL of the rendered PDF facsimile. Generated by
  // `npm run generate-pdfs` and committed under public/documents/.
  pdfUrl: string;
  payload: unknown;
}

function pdfUrlFor(id: string): string {
  return `/documents/${id}.pdf`;
}

